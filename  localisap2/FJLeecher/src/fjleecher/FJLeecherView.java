    class DownloadPage extends Thread{

        String url ="";
        JTextPane textArea;
        JProgressBar pb;
        WebUtils web = new WebUtils(Integer.parseInt(jTextField5.getText()));

        public DownloadPage(String url, JTextPane textArea,JProgressBar pb) {
            this.url = url;
            this.textArea = textArea;
            this.pb = pb;
        }
        
        @Override
        public void run() {
            textArea.setText("");
            pb.setIndeterminate(true);
            try {
                textArea.setText(web.downloadPage(url));
            } catch (Exception ex) {
                System.out.println(ex.getMessage());
                JOptionPane.showMessageDialog(null, "Error downloading page!");
            }
            pb.setIndeterminate(false);
        }
        
    }

