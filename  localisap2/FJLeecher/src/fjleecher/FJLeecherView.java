    @Action
    public void saveLeeched() {
        FileWriter fstream = null;
        final JFileChooser fc = new JFileChooser();
        ExampleFileFilter filter = new ExampleFileFilter();
        filter.addExtension("txt");
        fc.setFileFilter(filter);
        int returnVal = fc.showSaveDialog(null);
        if(returnVal == JFileChooser.APPROVE_OPTION){
            try {
                if (fc.getSelectedFile().getAbsolutePath().indexOf(".txt")<0)
                    fstream = new FileWriter(fc.getSelectedFile().getAbsolutePath()+".txt");
                else
                    fstream = new FileWriter(fc.getSelectedFile().getAbsolutePath());
                BufferedWriter out = new BufferedWriter(fstream);
                String text = jTextArea1.getText();
                int totalLines = jTextArea1.getLineCount();
                for (int i=0; i < totalLines; i++) {
                    int start=0;
                    int end=0;
                    start = jTextArea1.getLineStartOffset(i);
                    end   = jTextArea1.getLineEndOffset(i);
                    String line = text.substring(start, end);
                    if (i>0)
                        out.newLine();
                    out.write(line.replace("\n", ""));
                }
            out.close();
            } catch (Exception ex) {
                Logger.getLogger(FJLeecherView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
